// 단항연산자 Unary Operators
// + (양)
// - (음)
// ! (부정)

let a = 5;
console.log(-a);
console.log(!!a);
console.log(!!!a);
a = -a;
console.log(-a);
a = +a;
console.log(a);
a = +a;
console.log(a);
a = -a;
console.log(a);

let boolean = true;
console.log(boolean);
console.log(!boolean);

console.clear();
console.log(+false);
console.log(+true);
console.log(+null);
console.log(+'');
console.log(+'text');
console.log(+undefined);
